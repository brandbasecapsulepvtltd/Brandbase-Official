// Helper function to get blog by slug
export function getBlogBySlug(slug) {
  return BlogsData.find(blog => blog.metadata.slug === slug);
}

// Helper function to get blogs by category
export function getBlogsByCategory(category) {
  return BlogsData.filter(blog => blog.metadata.category === category);
}

// Helper function to get editor's picks
export function getEditorsPicks() {
  return BlogsData.filter(blog => blog.metadata.isEditorPick);
}

// Helper function to get slider blogs
export function getSliderBlogs() {
  return BlogsData.filter(blog => blog.metadata.isSlider); // Fixed typo: BLOGS_DATA → BlogsData
}

// Helper function to get helpful resources
export function getHelpfulResources() {
  return BlogsData.filter(blog => blog.metadata.isHelpfulResources);
}

