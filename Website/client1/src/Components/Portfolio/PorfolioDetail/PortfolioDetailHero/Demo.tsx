const AnimatedHeroDemo = () => {
  // 1. Define the ref to attach to the target element (the video wrapper)
  const triggerRef = useRef(null);
  
  // 2. Use the hook to track if the element is in view
  const isInView = useInView(triggerRef, { once: true, amount: 0.5 }); // Trigger once when 50% visible

  // Define the animation properties
  const videoVariants = {
    initial: { width: '90%' },
    animate: { width: isInView ? '100%' : '90%' },
  };

  return (
    <>
      {/* --- Section 1: Hero Component --- */}
      <AnimatedMarqueeHero
        tagline="Join over 100,000 happy creators"
        title={
          <>
            Engage Audiences
            <br />
            with Stunning Videos
          </>
        }
        description="Boost Your Brand with High-Impact Short Videos from our expert content creators. Our team is ready to propel your business forward."
        ctaText="Get Started"
        images={DEMO_IMAGES}
      />

      {/* --- Section 2: Video Scroll Animation --- */}
      <div
        ref={triggerRef} // Attach the ref here
        className="relative w-full h-[300px] sm:h-[700px] flex justify-center"
      >
        {/* 3. Use motion.video for Framer Motion capabilities */}
        <motion.video
          // Use the defined variants
          initial="initial"
          animate="animate"
          variants={videoVariants}
          transition={{ duration: 0.8, ease: 'easeOut' }} // Increased duration for smoother effect
          
          className="h-full object-cover rounded-xl shadow-2xl"
          src="https://www.pexels.com/download/video/4916733/"
          autoPlay
          loop
          muted
          playsInline
        />
      </div>
    </>
  );
};

export default AnimatedHeroDemo;